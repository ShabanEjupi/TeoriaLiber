// Enhanced navigation functions with proper styling
function showChapter(chapterNum) {
    if (chapterNum < 1 || chapterNum > totalChapters) return;
    
    currentChapter = chapterNum;
    const bookContent = document.getElementById('book-content');
    const content = document.getElementById('content-area');
    
    if (chapters[chapterNum]) {
        content.innerHTML = chapters[chapterNum].content;
        bookContent.style.display = 'block';
        document.body.classList.add('reading-chapter');
        
        // Hide all sections except book content
        ['home', 'about', 'chapters', 'testimonials', 'download', 'gallery'].forEach(id => {
            const section = document.getElementById(id);
            if (section) section.style.display = 'none';
        });
        
        // Smooth scroll to top
        window.scrollTo({ top: 0, behavior: 'smooth' });
        
        // Update navigation
        updateNavigation();
        
        // Process images after content load
        setTimeout(() => {
            if (typeof processAllImages === 'function') {
                processAllImages();
            }
        }, 100);
    }
}

function updateNavigation() {
    const navigation = document.querySelector('.navigation');
    const currentDisplay = document.getElementById('current-chapter-display');
    const prevBtn = navigation?.querySelector('button:first-child');
    const nextBtn = navigation?.querySelector('button:last-child');
    
    if (currentDisplay) {
        currentDisplay.textContent = `Kapitulli ${currentChapter}: ${chapters[currentChapter]?.title || ''}`;
    }
    
    if (prevBtn) {
        prevBtn.disabled = currentChapter <= 1;
        prevBtn.innerHTML = `← Kapitulli ${currentChapter - 1}`;
        prevBtn.style.cssText = `
            background: ${currentChapter <= 1 ? 'linear-gradient(135deg, #9ca3af 0%, #6b7280 100%)' : 'linear-gradient(135deg, #2E8B57 0%, #228B22 100%)'};
            color: white;
            border: none;
            padding: 15px 30px;
            border-radius: 25px;
            cursor: ${currentChapter <= 1 ? 'not-allowed' : 'pointer'};
            font-size: 16px;
            font-weight: 600;
            transition: all 0.3s ease;
            box-shadow: 0 8px 25px rgba(46, 139, 87, 0.2);
            min-width: 200px;
        `;
    }
    
    if (nextBtn) {
        nextBtn.disabled = currentChapter >= totalChapters;
        nextBtn.innerHTML = `Kapitulli ${currentChapter + 1} →`;
        nextBtn.style.cssText = `
            background: ${currentChapter >= totalChapters ? 'linear-gradient(135deg, #9ca3af 0%, #6b7280 100%)' : 'linear-gradient(135deg, #2E8B57 0%, #228B22 100%)'};
            color: white;
            border: none;
            padding: 15px 30px;
            border-radius: 25px;
            cursor: ${currentChapter >= totalChapters ? 'not-allowed' : 'pointer'};
            font-size: 16px;
            font-weight: 600;
            transition: all 0.3s ease;
            box-shadow: 0 8px 25px rgba(46, 139, 87, 0.2);
            min-width: 200px;
        `;
    }
}

function nextChapter() {
    if (currentChapter < totalChapters) {
        showChapter(currentChapter + 1);
    }
}

function prevChapter() {
    if (currentChapter > 1) {
        showChapter(currentChapter - 1);
    }
}

// Enhanced image error handling
function handleImageError(img, chapterNum) {
    console.log(`❌ Image not found for chapter ${chapterNum}: ${img.src}`);
    
    const placeholder = document.createElement('div');
    placeholder.className = 'image-placeholder';
    placeholder.innerHTML = `
        <div class="placeholder-content">
            <div class="islamic-pattern">
                <svg width="100" height="100" viewBox="0 0 100 100">
                    <pattern id="islamicPattern${chapterNum}" patternUnits="userSpaceOnUse" width="20" height="20">
                        <path d="M10,0 L20,10 L10,20 L0,10 Z" fill="#2E8B57" opacity="0.3"/>
                        <circle cx="10" cy="10" r="3" fill="#DAA520" opacity="0.7"/>
                    </pattern>
                    <rect width="100" height="100" fill="url(#islamicPattern${chapterNum})"/>
                </svg>
            </div>
            <h3>📖 Kapitulli ${chapterNum}</h3>
            <p>Imazhi është duke u ngarkuar...</p>
        </div>
    `;
    
    placeholder.style.cssText = `
        width: 100%;
        max-width: 600px;
        height: 300px;
        background: linear-gradient(135deg, #f8fafc 0%, #e2e8f0 100%);
        border: 2px dashed #2E8B57;
        border-radius: 15px;
        display: flex;
        align-items: center;
        justify-content: center;
        margin: 20px auto;
        text-align: center;
        color: #2E8B57;
    `;
    
    if (img.parentNode) {
        img.parentNode.replaceChild(placeholder, img);
    }
}

// Initialize the application
function initializeApp() {
    // Create main content structure if it doesn't exist
    if (!document.getElementById('book-content')) {
        const bookContentHTML = `
            <section id="book-content" class="book-content" style="display: none;">
                <div class="container">
                    <div class="chapter-header">
                        <button class="back-btn" onclick="goHome()">← Ktheu në Fillim</button>
                        <div class="chapter-actions">
                            <button class="print-btn" onclick="window.print()">🖨️ Printo</button>
                            <button class="download-btn" onclick="downloadChapter()">📥 Shkarko</button>
                        </div>
                    </div>
                    
                    <div id="content-area">
                        <!-- Chapter content will be loaded here -->
                    </div>
                    
                    <div class="navigation">
                        <button onclick="prevChapter()">← Kapitulli i Mëparshëm</button>
                        <div id="current-chapter-display">Kapitulli 1</div>
                        <button onclick="nextChapter()">Kapitulli i Ardhshëm →</button>
                    </div>
                </div>
            </section>
        `;
        
        document.body.insertAdjacentHTML('beforeend', bookContentHTML);
    }
    
    // Update navigation styling
    setTimeout(updateNavigation, 100);
}

function goHome() {
    document.getElementById('book-content').style.display = 'none';
    document.body.classList.remove('reading-chapter');
    
    // Show all sections
    ['home', 'about', 'chapters', 'testimonials', 'download', 'gallery'].forEach(id => {
        const section = document.getElementById(id);
        if (section) section.style.display = 'block';
    });
    
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

function downloadChapter() {
    const content = document.getElementById('content-area').innerHTML;
    const blob = new Blob([content], { type: 'text/html' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `Kapitulli-${currentChapter}.html`;
    a.click();
    URL.revokeObjectURL(url);
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', initializeApp);

// Make functions globally available
window.showChapter = showChapter;
window.nextChapter = nextChapter;
window.prevChapter = prevChapter;
window.goHome = goHome;
window.downloadChapter = downloadChapter;
window.handleImageError = handleImageError;
