    // Function to remove excessive uppercase and improve formatting
    function improveTextFormatting() {
        console.log('ğŸ”¤ Improving text formatting and reducing excessive uppercase...');
        
        if (!window.chapters) return;

        let improvedChapters = 0;

        Object.keys(window.chapters).forEach(chapterNumber => {
            let content = window.chapters[chapterNumber].content;
            
            // Fix excessive uppercase in headings
            content = content.replace(/(<h[1-6][^>]*>)([^<]*?)(<\/h[1-6]>)/gi, function(match, openTag, text, closeTag) {
                // Don't change if it's already properly formatted
                if (text.match(/^[A-Z][a-z]/)) return match;
                
                // Convert excessive uppercase to title case
                const titleCase = text.replace(/([A-Z]+)/g, function(upperText) {
                    if (upperText.length > 3) {
                        return upperText.charAt(0) + upperText.slice(1).toLowerCase();
                    }
                    return upperText;
                });
                
                return openTag + titleCase + closeTag;
            });
            
            // Fix excessive uppercase in regular text (but preserve acronyms and intentional caps)
            content = content.replace(/\b[A-Z]{4,}\b/g, function(match) {
                // Keep known acronyms
                const acronyms = ['HTML', 'CSS', 'JSON', 'HTTP', 'HTTPS', 'API', 'REST', 'AJAX', 'XML', 'SQL', 'PHP', 'CEO', 'CFO', 'CTO', 'USA', 'UAE', 'BMW', 'GDP', 'KOSOVA', 'SHQIPÃ‹RIA'];
                if (acronyms.includes(match)) return match;
                
                // Convert to title case for other long uppercase words
                return match.charAt(0) + match.slice(1).toLowerCase();
            });
            
            window.chapters[chapterNumber].content = content;
            improvedChapters++;
        });

        console.log(`âœ… Improved formatting for ${improvedChapters} chapters`);
    }

