// Search functionality
function searchContent(query) {
    if (!query || query.trim() === '') {
        alert("Ju lutem shkruani një term për kërkim.");
        return;
    }
    
    query = query.toLowerCase().trim();
    let results = [];
    
    // Search through all chapters
    for (let i = 1; i <= totalChapters; i++) {
        const title = getChapterTitle(i).toLowerCase();
        const content = (chapters[i] ? chapters[i].content : generateChapterContent(i, getChapterTitle(i))).toLowerCase();
        
        if (title.includes(query) || content.includes(query)) {
            results.push({
                chapter: i,
                title: getChapterTitle(i),
                relevance: title.includes(query) ? 'title' : 'content'
            });
        }
    }
    
    if (results.length === 0) {
        alert(`❌ Nuk u gjetën rezultate për "${query}". Provoni me fjalë të tjera.`);
        return;
    }
    
    // Display results
    let resultMessage = `🔍 U gjetën ${results.length} rezultate për "${query}":\n\n`;
    results.slice(0, 10).forEach((result, index) => {
        resultMessage += `${index + 1}. Kapitulli ${result.chapter}: ${result.title}\n`;
    });
    
    if (results.length > 10) {
        resultMessage += `\n... dhe ${results.length - 10} rezultate të tjera.`;
    }
    
    resultMessage += `\n\nDëshironi të hapni rezultatin e parë?`;
    
    if (confirm(resultMessage)) {
        showChapter(results[0].chapter);
    }
}

