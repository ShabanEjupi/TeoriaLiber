// Funksionet e tjera të script-it
function showChapter(chapterNumber) {
    if (chapterNumber < 1 || chapterNumber > totalChapters) {
        alert('Kapitulli nuk ekziston!');
        return;
    }

    currentChapter = chapterNumber;
    
    // Fshih section-et kryesore
    const mainSections = document.querySelectorAll('section');
    mainSections.forEach(section => section.style.display = 'none');
    
    // Trego chapter display
    let chapterDisplay = document.getElementById('chapterDisplay');
    if (!chapterDisplay) {
        chapterDisplay = document.createElement('section');
        chapterDisplay.id = 'chapterDisplay';
        chapterDisplay.className = 'chapter-display';
        document.body.appendChild(chapterDisplay);
    }
    
    chapterDisplay.style.display = 'block';
    
    const chapter = chapters[chapterNumber];
    if (chapter) {
        chapterDisplay.innerHTML = `
            <div class="container">
                <div class="chapter-header">
                    <button onclick="goBackToChapters()" class="back-btn">← Kthehu tek Lista</button>
                    <div class="chapter-actions">
                        <button onclick="printChapter()" class="print-btn">🖨️ Printo</button>
                        <button onclick="downloadChapter()" class="download-btn">📥 Shkarko</button>
                    </div>
                </div>
                <div class="chapter-content">
                    ${chapter.content}
                </div>
                <div class="chapter-navigation">
                    ${chapterNumber > 1 ? `<button onclick="showChapter(${chapterNumber - 1})" class="nav-btn prev-btn">← Kapitulli ${chapterNumber - 1}</button>` : ''}
                    ${chapterNumber < totalChapters ? `<button onclick="showChapter(${chapterNumber + 1})" class="nav-btn next-btn">Kapitulli ${chapterNumber + 1} →</button>` : ''}
                </div>
            </div>
        `;
        
        // Update URL
        history.pushState({chapter: chapterNumber}, '', `#chapter-${chapterNumber}`);
        
        // Scroll to top
        window.scrollTo(0, 0);
    }
}

function goBackToChapters() {
    const chapterDisplay = document.getElementById('chapterDisplay');
    if (chapterDisplay) {
        chapterDisplay.style.display = 'none';
    }
    
    const mainSections = document.querySelectorAll('section');
    mainSections.forEach(section => {
        if (section.id !== 'chapterDisplay') {
            section.style.display = 'block';
        }
    });
    
    // Update URL
    history.pushState({}, '', '#chapters');
    
    // Scroll to chapters section
    const chaptersSection = document.getElementById('chapters');
    if (chaptersSection) {
        chaptersSection.scrollIntoView();
    }
}

function printChapter() {
    const chapterContent = document.querySelector('.chapter-content');
    if (!chapterContent) return;
    
    const printWindow = window.open('', '', 'height=600,width=800');
    printWindow.document.write(`
        <html>
            <head>
                <title>Kapitulli ${currentChapter}</title>
                <style>
                    body { font-family: Arial, sans-serif; line-height: 1.6; margin: 20px; }
                    .islamic-quote { background: #f0f9ff; padding: 15px; margin: 15px 0; border-left: 4px solid #0ea5e9; font-style: italic; text-align: center; }
                    .highlight-box { background: #f0fdf4; padding: 15px; margin: 15px 0; border-left: 4px solid #22c55e; }
                    h2 { color: #2E8B57; }
                    h3 { color: #006400; }
                </style>
            </head>
            <body>
                ${chapterContent.innerHTML}
            </body>
        </html>
    `);
    printWindow.document.close();
    setTimeout(() => { printWindow.print(); }, 500);
}

