// Event listeners
document.addEventListener('DOMContentLoaded', function() {
    // Load chapters on page load
    loadChapterGrid();
    
    // Handle browser back/forward
    window.addEventListener('popstate', function(event) {
        if (event.state && event.state.chapter) {
            showChapter(event.state.chapter);
        } else {
            goBackToChapters();
        }
    });
});

function loadChapterGrid() {
    const chapterGrid = document.getElementById('chapterGrid');
    if (!chapterGrid) return;
    
    let gridHTML = '';
    
    for (let i = 1; i <= totalChapters; i++) {
        const chapter = chapters[i];
        const status = i <= 10 ? 'I Plotë' : 'Në Zhvillim';
        
        gridHTML += `
            <div class="chapter-card" onclick="showChapter(${i})">
                <div class="chapter-number">Kapitulli ${i}</div>
                <h3>${chapter.title}</h3>
                <div class="chapter-preview">
                    Strategji të avancuara për suksesin tuaj personal dhe profesional...
                </div>
                <div class="chapter-status">${status}</div>
            </div>
        `;
    }
    
    chapterGrid.innerHTML = gridHTML;
}

